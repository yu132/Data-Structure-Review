package ag.common.dfsAndBfs;

public class Introducing {

	/**
	 * DFS和BFS是非线性搜索里面非常常用的两种思想
	 * 
	 * DFS是优先先将一条路径搜索完毕，然后再考虑其余一条路径的搜索
	 * 
	 * BFS是每条路都考虑，每次都走一步
	 * 
	 * 两者都是很好的思想，只不过根据不同的场合我们往往需要选择需要使用
	 * 的方法，并不是所有情况都可以使用
	 * 
	 * 例如搜索区域无穷大的时候，我们不能使用DFS，因为可能一条路走到黑
	 * 然后发现越走越远，这个时候我们往往只能使用BFS
	 * 
	 * BFS还有多方向的说法，我们可以从多个节点一起出发，直到某些路径相遇
	 */

}
